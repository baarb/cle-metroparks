package com.techelevator;

public class Animal {
	private String animalId;
	private String animalTitle;
	private String animalBlurb;
	
	
	
	public String getAnimalId() {
		return animalId;
	}
	public void setAnimalId(String animalId) {
		this.animalId = animalId;
	}
	public String getAnimalTitle() {
		return animalTitle;
	}
	public void setAnimalTitle(String animalTitle) {
		this.animalTitle = animalTitle;
	}
	public String getAnimalBlurb() {
		return animalBlurb;
	}
	public void setAnimalBlurb(String animalBlurb) {
		this.animalBlurb = animalBlurb;
	}
}
